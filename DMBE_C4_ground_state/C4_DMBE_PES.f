      TWOBODY(3)=DYN(3)*(1-SWIT(R1,R2,R3))*(1-SWIT(R2,R3,R1))

      TWOBODYDYN=SUM(TWOBODY)

      TWOBODYHF=SUM(EHF)

      CALL POT3DC(R1,R2,R3,VDC3)
  
