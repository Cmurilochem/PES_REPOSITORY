      SUBROUTINE POTC4(R,POTTOT)
      IMPLICIT NONE
      DOUBLE PRECISION, DIMENSION(6) :: R 
      DOUBLE PRECISION :: V4APPROX, POT23, POTTOT

      CALL TOTADDFOURBD(R,V4APPROX)

      CALL POT23C4(R,POT23)

      POTTOT=POT23+V4APPROX

      END SUBROUTINE POTC4

