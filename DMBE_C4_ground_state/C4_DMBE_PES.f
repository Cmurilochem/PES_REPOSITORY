C###############################################################################
C C4 POTENTIAL WITHOUT 4-BODY TERMS
C###############################################################################

      SUBROUTINE POT23C4(R,POT)
      IMPLICIT NONE
      DOUBLE PRECISION, DIMENSION(6) :: R 
      DOUBLE PRECISION, DIMENSION(6) :: V2EHF, V2DC, VCC
      DOUBLE PRECISION, DIMENSION(4) :: V3DC
      DOUBLE PRECISION, DIMENSION(4) :: V3EHF
      DOUBLE PRECISION :: TWOBODYEHF, TWOBODYDC
      DOUBLE PRECISION :: THREEBODYEHF, THREEBODYDC
      DOUBLE PRECISION :: POT
   
      CALL POT2BODY(R(1),V2EHF(1),V2DC(1),VCC(1))

      CALL POT2BODY(R(2),V2EHF(2),V2DC(2),VCC(2))

      CALL POT2BODY(R(3),V2EHF(3),V2DC(3),VCC(3))
 
      CALL POT2BODY(R(4),V2EHF(4),V2DC(4),VCC(4))

      CALL POT2BODY(R(5),V2EHF(5),V2DC(5),VCC(5))

      CALL POT2BODY(R(6),V2EHF(6),V2DC(6),VCC(6))

      CALL THRBODYSPECC3(R(1),R(2),R(6),V3EHF(1))

      CALL THRBODYSPECC3(R(2),R(3),R(4),V3EHF(2))

      CALL THRBODYSPECC3(R(4),R(5),R(6),V3EHF(3))

      CALL THRBODYSPECC3(R(1),R(3),R(5),V3EHF(4))

      CALL POT3DC(R(1),R(2),R(6),V3DC(1))

      CALL POT3DC(R(2),R(3),R(4),V3DC(2))

      CALL POT3DC(R(4),R(5),R(6),V3DC(3))

      CALL POT3DC(R(1),R(3),R(5),V3DC(4))

      TWOBODYEHF=SUM(V2EHF)

      TWOBODYDC=SUM(V2DC)

      THREEBODYEHF=SUM(V3EHF)

      THREEBODYDC=SUM(V3DC)

      POT=TWOBODYEHF+THREEBODYEHF+TWOBODYDC+ 
     &    THREEBODYDC

      END SUBROUTINE POT23C4

C###############################################################################
C FINDING THREE BODY ENERGIES
C###############################################################################

      SUBROUTINE THRBODYSPECC3(R1,R2,R3,THREEBDSPEC)
      IMPLICIT NONE
      DOUBLE PRECISION :: R1, R2, R3
      DOUBLE PRECISION, DIMENSION(3) :: R
      DOUBLE PRECISION :: SCALEDPOT23TOT
      DOUBLE PRECISION :: POTTOT
      DOUBLE PRECISION :: THREEBDSPEC
      DOUBLE PRECISION :: IFN

      R(1)=R1
      R(2)=R2
      R(3)=R3

      CALL SCALEDDYNPOT23(R(1),R(2),R(3),SCALEDPOT23TOT)

      CALL POTC3SPEC(R,POTTOT)

      IFN=50.000D+00

C JUST IN CASE, SET BY BRUTE FORE THE THREE-BODY AS ZERO BEYOND 50 BORHS
      IF (R(1).GE.IFN .OR. R(2).GE.IFN .OR. R(3).GE.IFN) THEN 
        THREEBDSPEC=0.0D+00
      ELSE
        THREEBDSPEC=POTTOT-SCALEDPOT23TOT
      END IF

      END SUBROUTINE THRBODYSPECC3

C###############################################################################
C ENERGY-SWITCHING DMBE/ES/SS POTENTIAL
C###############################################################################

      SUBROUTINE POTC3SPEC(R,POT)
      IMPLICIT NONE
      INTEGER :: N
      DOUBLE PRECISION, PARAMETER :: CM2EH=219474.63137050000000D+00
      DOUBLE PRECISION, DIMENSION(3) :: R
      DOUBLE PRECISION :: V1, V2, POT
      DOUBLE PRECISION :: EMIN, EREF
      DOUBLE PRECISION :: DE, DEREF
      DOUBLE PRECISION :: ES, F
      DOUBLE PRECISION :: BETA
      DOUBLE PRECISION, PARAMETER :: ZERO=1.000D-12
      DOUBLE PRECISION :: POTZERO

      EMIN=-0.5227686755824460000D+00 
C SUM OF DE OF DIATOMIC PLUS GROUND STATE ENERGY
      EREF=EMIN+(6500.0000D+00/CM2EH)

      CALL POTC3(R,V1)
      CALL SSC3(R,V2)

      IF (V1==EMIN) THEN
        DE=ZERO
      ELSE 
        DE=(V1-EMIN)
      END IF

      DEREF=(EREF-EMIN)

      BETA=15.0000D+00
      N=2

      F=ES(BETA,DE,DEREF,N)

      POTZERO=0.00000000000000000D+00

      POT=(V2+(V1-V2)*F)-POTZERO

      END SUBROUTINE POTC3SPEC

C###############################################################################
