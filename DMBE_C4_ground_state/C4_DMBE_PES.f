      RANG(3)=(1.00D+00-TANH(GAM1*(R2-X1)))*
     &        (1.00D+00-TANH(GAM1*(R3-X2)))*
     &        (1.00D+00-TANH(GAM1*(R1-X2)))
      RANGC2V=RANG(1)+RANG(2)+RANG(3)
