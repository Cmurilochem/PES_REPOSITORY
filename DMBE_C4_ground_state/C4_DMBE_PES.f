      RANG(1)=(1.00D+00-TANH(GAM1*(R1-X1)))*
     &        (1.00D+00-TANH(GAM1*(R2-X2)))*
     &        (1.00D+00-TANH(GAM1*(R3-X2)))
      RANG(2)=(1.00D+00-TANH(GAM1*(R3-X1)))*
     &        (1.00D+00-TANH(GAM1*(R1-X2)))*
